package 연산자;

public class 비교연산자 {

	public static void main(String[] args) {
		// 산술연산자(사칙연산, 나누기에서 몫과 나머지 구하기)
		int x = 20;
		int y = 10;

		System.out.println("같다" + (x == y));
		System.out.println("다르다" + (x != y));
		System.out.println("크다 " + (x > y));
		System.out.println("크거나 같다 " + (x >= y));
	}

}
